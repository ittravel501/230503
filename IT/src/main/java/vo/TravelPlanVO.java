package vo;


public class TravelPlanVO {
	
	private int city_num;	
	private String city_name;
	private String city_eng;
	private String city_con;
	private String city_exp;
	

	public String getCity_exp() {
		return city_exp;
	}
	public void setCity_exp(String city_exp) {
		this.city_exp = city_exp;
	}
	public int getCity_num() {
		return city_num;
	}
	public void setCity_num(int city_num) {
		this.city_num = city_num;
	}
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}
	public String getCity_eng() {
		return city_eng;
	}
	public void setCity_eng(String city_eng) {
		this.city_eng = city_eng;
	}
	public String getCity_con() {
		return city_con;
	}
	public void setCity_con(String city_con) {
		this.city_con = city_con;
	}
	
}